#include "vector.h"

///////////////////////////////////////////////////////////////////////////////
//
// vector_norm -> Normalize vectors
//
///////////////////////////////////////////////////////////////////////////////
VECTOR vector_norm (VECTOR vector){
    float norm = sqrt(pow(vector.x,2) + pow(vector.y,2) + pow(vector.z,2));
    vector.x = vector.x / norm;
    vector.y = vector.y / norm;
    vector.z = vector.z / norm;
    return vector;
}

///////////////////////////////////////////////////////////////////////////////
//
// vector_dot_prod -> Execute the dot product of two vectors
//
///////////////////////////////////////////////////////////////////////////////
float vector_dot_prod (VECTOR v1, VECTOR v2){
    float dot_prod;
    return dot_prod = (v1.x*v2.x + v1.y*v2.y + v1.z*v2.z);
}

///////////////////////////////////////////////////////////////////////////////
//
// vector_cross_prod -> Execute the cross product of two vectors
//
///////////////////////////////////////////////////////////////////////////////
VECTOR vector_cross_prod (VECTOR v1, VECTOR v2){
    VECTOR cross_prod;
    cross_prod.x = v1.y*v2.z - v1.z*v2.y;
    cross_prod.y = v1.z*v2.x - v1.x*v2.z;
    cross_prod.z = v1.x*v2.y - v1.y*v2.x;
    return cross_prod;
}

///////////////////////////////////////////////////////////////////////////////
//
// vector_magnitude -> Calculate the magnitud of a vector
//
///////////////////////////////////////////////////////////////////////////////
float vector_magnitude (VECTOR v1){
    float magnitude = sqrt(pow(v1.x,2) + pow(v1.y,2) + pow(v1.z,2));
    return magnitude;
}

